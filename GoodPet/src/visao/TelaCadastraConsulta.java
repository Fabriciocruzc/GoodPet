       c.setHorario(jFormattedTextFieldHorarioConsulta.getText());
       cont.Editar(c);
       
       jTextFieldIdConsulta.setText("");
       jTextFieldAnimalConsulat.setText("");
       jComboBoxDonoConsulta.setToolTipText("");
       jComboBoxVeterinarioConsulta.setToolTipText("");
